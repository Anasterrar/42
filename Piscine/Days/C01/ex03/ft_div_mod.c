#include <stdio.h>

void	ft_div_mod(int a, int b, int *div, int *mod)
{
	*div = a / b;
	*mod = a % b;
}

int	main(void)
{
	int	div;
	int	mod;

	ft_div_mod(11, 2, &div, &mod);
	printf("div => %d\nmod => %d\n", div, mod);
	return (0);
}
