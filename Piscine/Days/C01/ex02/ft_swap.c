#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

int	main(void)
{
	int	a;
	int	b;

	a = 10;
	b = 22;
	printf("Befor swap:\n");
	printf("a => %d\nb => %d\n", a, b);
	ft_swap(&a, &b);
	printf("After swap:\n");
	printf("a => %d\nb => %d\n", a, b);
	return (0);
}
