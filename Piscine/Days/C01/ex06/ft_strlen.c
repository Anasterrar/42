#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

int	main(void)
{
	char	word[] = "";

	printf("Mot: %s\nlen: %d\n", word, ft_strlen(word));
	return (0);
}
