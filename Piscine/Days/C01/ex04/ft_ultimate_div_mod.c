#include <stdio.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	tmp1;
	int	tmp2;

	tmp1 = *a / *b;
	tmp2 = *a % *b;
	*a = tmp1;
	*b = tmp2;
}

int	main(void)
{
	int	a;
	int	b;

	a = 11;
	b = 2;
	ft_ultimate_div_mod(&a, &b);
	printf("a => %d\nb => %d\n", a, b);
	return (0);
}
